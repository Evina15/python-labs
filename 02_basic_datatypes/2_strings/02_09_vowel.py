'''
Write a script prints the number of times a vowel is used in a user inputted string.

'''

sentence = input("Please enter a sentence: ")

# counting a
a = sentence.count("a")
print("Total number of a: " ,a)

# counting i
i = sentence.count("i")
print("Total number of i: " ,i)

# counting u
u = sentence.count("u")
print("Total number of u: " ,u)

# counting e
e = sentence.count("e")
print("Total number of e: " ,e)

# counting o
o = sentence.count("o")
print("Total number of o: " ,o)
